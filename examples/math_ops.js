function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

function plus(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function sqrt(a) {
    return Math.sqrt(a);
}

function exp(a, b) {
    return a - b;
}

function matrix([[a,b],[c,d]]) {
    return [[a, b], [c, d]];
}